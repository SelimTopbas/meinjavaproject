package day05operators;

public class Increment04 {

	public static void main(String[] args) {

		
		//Int data tipinde bir variable oluştur. bunu her seferinde 1 arttırmak için 3 yöntemi de kullanın
		
		int sayi1 = 20;
		sayi1 = sayi1 + 1;
		sayi1 += 1;
		sayi1++;
		System.out.println(sayi1);
	}

}
