package day05operators;

public class Increment03 {

	public static void main(String[] args) {

		// int data tipinde iki tane variable oluşturun
		//bu variable leri en kısa yolu kullanara bir arttırın.
		// Not: En kısa yol sadece 1 arttırmak içindir
		
		int sayi1 = 12;
		sayi1 ++; // variable isminden sonra ++ koyarsak o vriablenin değerin 1 artmış olur
		System.out.println(sayi1);
		
		
		int sayi2 = 20;
		sayi2 ++;
		System.out.println(sayi2);

	}

}
